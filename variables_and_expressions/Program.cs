using System;

namespace UsingVariables
{
    class Program
    {
        static void Main(string[] args)
        {
            char aChar = 'a'; //字元(97'a')
            Console.WriteLine(aChar);
            int anInt = 123; 
            Console.WriteLine(anInt);
            double aDouble;
            aDouble = 123.456; //初值
            Console.WriteLine(aDouble);
            bool aBool = true; //布林代數(判斷真偽)
            Console.WriteLine(aBool);
        }
    }
}
